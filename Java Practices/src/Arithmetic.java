public class Arithmetic {
    public static void main(String[] args) {
        int a=5;
        int b=6;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        double x=10;
        double y=3;
        System.out.println(x+y);
        System.out.println(x*y);
        System.out.println(x-y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}
